import { ref, onMounted } from 'vue';


const hertas = ref([]);
const modal = ref(null);
const selectedHerta = ref({}); // 或者你认为合适的默认值

const fetchHertas = async () => {
  try {
    const data = await queryContent('/herta').find();
    console.log('Fetched data:', data); // 添加调试信息
    hertas.value = data;
  } catch (error) {
    console.error('Failed to fetch Hertas:', error);
  }
};

const openModal = async (herta) => {
  console.log('Opening modal for herta:', herta);
  selectedHerta.value = herta;
  try {
    const content = await queryContent(herta._path).findOne();
    console.log('Fetched content:', content); // 添加调试信息
    selectedHerta.value.content = content.body;
    const modalElement = document.getElementById('myModal');
    if (modalElement && modalElement.showModal) {
      modalElement.showModal();
    } else {
      console.error('showModal is not supported');
    }
  } catch (error) {
    console.error('Failed to fetch content:', error);
  }
};

onMounted(async () => {
  await fetchHertas();
  console.log('Hertas fetched:', hertas.value); // 添加调试信息
  const closeButton = document.getElementById('close-drawer');
  const drawerInput = document.getElementById('my-drawer');

  if (closeButton && drawerInput) {
    closeButton.addEventListener('click', () => {
      drawerInput.checked = false;
    });
  } else {
    console.error('Element not found');
  }
});
